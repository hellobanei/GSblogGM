// Get the iframe element
const videoElement = document.getElementById('myVideo');

// Check for browser support of the 'Page Visibility API'
if (typeof document.visibilityState === 'undefined') {
  // If not supported, fallback to a visibility change event
  window.addEventListener('visibilitychange', handleVisibilityChange);
} else {
  // If supported, use the Page Visibility API
  document.addEventListener('visibilitychange', handleVisibilityChange);
}

function handleVisibilityChange() {
  // Check if the page is visible
  if (document.visibilityState === 'visible') {
    // If visible, play the video
    videoElement.play();
  } else {
    // If not visible, pause the video
    videoElement.pause();
  }
}
